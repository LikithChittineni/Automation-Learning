import math
print("Constants:")
radius=10
area=math.pi*(radius**2)
print(f"The area of circle is with radius {radius} is :{area} ")
circ=2*(math.pi*radius)
print(f"The circumference of circle with radius {radius} is :{circ} ")

num=29
power=math.pow(num,2)
print(f"the power of {num} is :{power} ")
sqr=math.sqrt(num)
print(f"the square root of {num} is :{sqr} ")

angle_degree=90
radians=math.radians(angle_degree)
sin=math.sin(angle_degree)
print(f"the radians of {angle_degree} is :{radians} ")
print(f"the sin of {angle_degree} is :{sin} ")

x=20
log=math.log(x)
exp=math.exp(x)
print(f"the log of {x} is :{log} ")
print(f"the exp of {x} is :{exp} ")
